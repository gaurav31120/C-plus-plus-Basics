#include <iostream>
using namespace std;

int main()
{
    cout << "My name is Gaurav"; // My name is Gaurav

    cout << "I am from India";

    // endl ---- this will take you to the next line.
    // /n ---- this will also take you to the next line but the syntax writing is different than endl
    cout << "This is first line" << endl;

    cout << "This is third line \n"; // syntax of \n is inside the double quotes(" \n")

    cout << "This is fourth line";

    return 0;
}
