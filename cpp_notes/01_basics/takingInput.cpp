#include <iostream>
using namespace std;

int main()
{
    int a = 10, b = 20;
    // cin>> is used to take input from the system

    // << -- insertion operator
    // >> -- extraction operator

    // use space or enter to give multiple inputs

    cin >> a;      // 5
    cin >> b;      // 6
    cout << a + b; // 11

    return 0;
}